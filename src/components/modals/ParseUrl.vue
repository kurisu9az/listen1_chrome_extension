<template>
  <DefaultModal>
    <template #header>
      <h3>{{ t('_PLAYLIST_LINK') }}</h3>
    </template>
    <template #body>
      <div class="dialog-open-url">
        <input class="form-control" type="text" :placeholder="t('_EXAMPLE') + 'https://www.xiami.com/collect/198267231'" v-model="url" />
      </div>
    </template>
    <template #footer>
      <button class="btn btn-primary confirm-button" @click="openUrl()">{{ t('_CONFIRM') }}</button>
      <button class="btn btn-default" @click="$emit('close')">{{ t('_CANCEL') }}</button>
    </template>
  </DefaultModal>
</template>

<script setup lang="ts">
import DefaultModal from './DefaultModal.vue';
import { useI18n } from 'vue-i18n';
import MediaService from '../../services/MediaService';
import { useRouter } from 'vue-router';

const router = useRouter();
const { t } = useI18n();
let url = $ref('');
const emit = defineEmits(['close']);

const showPlaylist = (playlistId: string) => {
  router.push('/playlist/' + playlistId);
};

const openUrl = async () => {
  const result = await MediaService.parseURL(url);
  if(result !== null){
    showPlaylist(result.id)
  }
  emit('close');
}

</script>

<script lang="ts">
export default {};
</script>


